package org.example;

public class Exercice4 {
    public static void main(String[] args) {
        Integer valeurDuDe = 4; // valeur de 1 à 6

        switch (valeurDuDe) {
            case 1 :
                System.out.println("un");
            break;
            case 2 :
                System.out.println("deux");
                break;
            case 3 :
                System.out.println("trois");
                break;
            case 4 :
                System.out.println("quatre");
                break;
            case 5 :
                System.out.println("cinq");
                break;
            case 6 :
                System.out.println("six");

        }
        // Utiliser l'instruction switch pour afficher en toutes lettres la valeur associée à chaque face d'un dé
    }
}
